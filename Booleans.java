

public class Booleans {
    public static void main(String[] args) {
        double change = 3.50;
        double chips = 2.50;
        System.out.println("Hi Java, do i have enough change to buy chips?");
        System.out.println(chips <= change);

        int people = 15;
        int elevatorholds = 12;
        System.out.println("Hi Java, can the elevator hold everyone?");
        System.out.println(elevatorholds <= people);

        
    }
}